    {
        //
        $transaction = new Transaction;
        $transaction->account_id=$request->account_id;
        $transaction->Reference=$request->Reference;
        $transaction->In=$request->In;
        $transaction->Out=$request->Out;

        $transaction->save();
        return view('/Mybank/index');


